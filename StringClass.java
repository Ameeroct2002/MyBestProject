public class StringClass {
    public static void main(String args[]) {
        String[] word = {"cat", "pat", "word", "road"};
        for (int w = 0; w < word.length; w++) {
            System.out.println(word[w]);
        }
        double[] array = {0.6, 3.5, 9.8, 2.9};
        for (int x = 0; x < array.length; x++) {
            System.out.println(array[x]);
        }
        boolean[] drake ={true,false,true,false,true};
        for (int y=0;y< drake.length;y++)
        {
            System.out.println(drake[y]);
        }
        char[] ariana ={'a','s','f','t','d'};
        for (int z=0;z<ariana.length;z++)
        {
            System.out.println(ariana[z]);
        }
    }
}
